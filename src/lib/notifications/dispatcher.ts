/**
 * Send a direct email (not template-based) — e.g. to a vendor.
 * Used for vendor assignment notifications where there's no tenant template.
 */
export async function sendDirectEmail(
  to: string,
  subject: string,
  html: string
) {
  const { sendEmail } = await import("./email")
  return sendEmail(to, subject, html)
}

